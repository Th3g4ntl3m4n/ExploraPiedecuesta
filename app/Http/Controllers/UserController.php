     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::all();
        foreach ($users as $user) {
          $agency = Agency::where('agent', '=', $user->id)->get();
          $data_agent = $agency->toArray();

          if($data_agent){
            $user->company = $data_agent[0]["name"];
            $user->company_id = $data_agent[0]["id"];
          }else{
            $user->company = "No aplica";
          }

        }

        return view('users.index')->with('users', $users);
    }

